package SecondLesson.bonustask04;

/**
 * Derive table of squares first positive integers from 0 up to 10
 * 
 * @author Hedgehog
 *
 */
public class Solution {
	public static void main(String[] args) {
		System.out.println("Number" + "\t" + "Square");
		for (int i = 0; i <= 10; i++) {
			System.out.println(i + "\t" + i * i + " ");
		}
	}
}
